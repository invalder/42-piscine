#include <unistd.h>

char	*ft_strupcase(char *str)
{
	char	*buff;

	buff = str;
	while (*buff != '\0')
	{
		if (*buff >= 'a' && *buff <= 'z')
			*buff -= 32;
		buff++;
	}
	return (str);
}
